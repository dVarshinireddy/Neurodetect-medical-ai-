def get_all_doctors():
    return DOCTORS

def get_doctor_by_id(doc_id):
    for d in DOCTORS:
        if d["id"] == doc_id:
            return d
    return None
def get_doctors_by_specialty(specialty):
    return [
        d for d in DOCTORS
        if d["specialty"].lower()
        == specialty.lower()
    ]
def get_recommended_doctors(tumor_type):
    ids = TUMOR_DOCTOR_MAP.get(
        tumor_type, []
    )
    return [
        get_doctor_by_id(i)
        for i in ids
        if get_doctor_by_id(i)
    ]
def get_emergency_doctors():
    return [
        d for d in DOCTORS
        if d["emergency"]
    ]

def get_specialties():
    return list(set([
        d["specialty"] for d in DOCTORS
    ]))
